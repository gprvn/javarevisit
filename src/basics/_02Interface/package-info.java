package basics._02Interface;

/**
*	INTERFACE
*	-Interface are 100% abstract classes i.e. an interface will not have even a single implementation.
*	 we can't create an object.
*	
*	-by default every method inside an interface is public and abstract and every attribute is static and final.
*
*	-inside interface we can declare methods and attributes, we can't define methods, can't keep constructor, IIB, SIB.
*
*	-we can't have non static attributes inside interface.
*
*	-we can't have static methods inside interface.
*
*	-one interface can extends more than one interface.
*
*	-compiler generates .class file for interface.
*
*	-through interface we can achieve 100% abstraction.
*/