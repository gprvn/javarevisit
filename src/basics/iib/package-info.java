package basics.iib;

/**

##################################
#               IIB    		     #
##################################

***************Instance Initialisation Block***********
*
***This block doesn't have name neither is is static.
*
***This is termed as IIB Instance Initialisation Block.
*
***This is executed before execution of Constructor, as object is creating.
*
***We can not call main method from IIB as recursion occurs.
*
***IIB's get executed for every constructor call.
*
***IIB's are used for executing same code again and again.
*
***IIB executed no matter which constructor is executing.

*/